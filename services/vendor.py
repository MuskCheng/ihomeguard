"""设备厂商识别 - 通过 MAC 地址 OUI 查询"""
import os
import requests
from functools import lru_cache

# OUI 数据库 URL
OUI_DB_URL = "https://standards-oui.ieee.org/oui.txt"
OUI_CACHE_FILE = "data/oui_cache.txt"


@lru_cache(maxsize=1000)
def get_vendor(mac: str) -> str:
    """通过 MAC 地址获取设备厂商
    
    MAC 地址前 3 字节 (OUI) 用于标识厂商
    """
    if not mac:
        return "Unknown"
    
    # 提取 OUI (前 6 个字符，不含分隔符)
    oui = mac.upper().replace(':', '').replace('-', '')[:6]
    
    if len(oui) < 6:
        return "Unknown"
    
    # 查询本地缓存
    vendor = _lookup_local(oui)
    if vendor:
        return vendor
    
    # 在线查询
    vendor = _lookup_online(oui)
    return vendor or "Unknown"


def _lookup_local(oui: str) -> str:
    """本地缓存查询"""
    cache_path = os.path.join(os.path.dirname(__file__), '..', OUI_CACHE_FILE)
    
    if not os.path.exists(cache_path):
        return None
    
    try:
        with open(cache_path, 'r', encoding='utf-8') as f:
            for line in f:
                if line.startswith(oui):
                    # 格式: OUI|Vendor
                    parts = line.strip().split('|', 1)
                    if len(parts) > 1:
                        return parts[1]
    except:
        pass
    
    return None


def _lookup_online(oui: str) -> str:
    """在线查询 OUI 数据库"""
    try:
        # 使用 macvendors.co API
        resp = requests.get(f"https://api.macvendors.com/{oui}", timeout=5)
        if resp.status_code == 200:
            return resp.text.strip()
    except:
        pass
    
    return None


